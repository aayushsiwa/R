cat("num1 > num2: ", num1 > num2, "\n")
cat("num1 < num2: ", num1 < num2, "\n")
cat("num1 >= num2: ", num1 >= num2, "\n")
cat("num1 <= num2: ", num1 <= num2, "\n")
